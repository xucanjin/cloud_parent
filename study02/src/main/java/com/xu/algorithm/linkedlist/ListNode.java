package com.xu.algorithm.linkedlist;

/**
 * @author xucanjin
 * @date 2022/03/30
 * @description
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
